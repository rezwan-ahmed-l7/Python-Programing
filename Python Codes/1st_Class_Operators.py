# Arithmetic Operators

a = 5
b = 2

sum = (a + b)

print ("Sum of a and b is: ", sum)
print ("Sum: ", a + b)   # Sum

print ("Modulus: ", a % b)   # Modulus to get remainder
print ("Exponentiation: ", a ** b)  # Exponentiation to get power
print ("Floor Division: ", a // b)  # Floor division to get integer value
print ( )

# Assignment Operators

c = 10
d = 5

c **= d  # c = c ** d
print ("c to the power of d: ", c)

# Logical Operators

e = 10
f = 5

print (not (e > f))
print (e > f and e < 15)
print (e > f or e < 15)

value1 = True
value2 = False

print (value1 and value2)
print (value1 or value2)
print (not value1)